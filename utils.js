export function add(additionNum1, additionNum2) {
    return Number(additionNum1) + Number(additionNum2);
} 

export function subtraction(subtractionNum1, subtractionNum2) {
    return Number(subtractionNum1) - Number(subtractionNum2);
}

export function multiplication(multiplicationNum1, multiplicationNum2) {
    return Number(multiplicationNum1) * Number(multiplicationNum2);
}

export function division(divisionNum1, divisionNum2) {
    return Number(divisionNum1) / Number(divisionNum2);
}